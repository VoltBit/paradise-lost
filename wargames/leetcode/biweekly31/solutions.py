class Solution:
  def countOdds(self, low: int, high: int) -> int:
    print(f'check:{len([x for x in range(low, high+1) if x%2==1])}')
    if high % 2 == 0 and low % 2 == 0:
      return int((high - low)/2)
    elif high % 2 == 1 and low % 2 == 1:
      return int((high - low)/2) + 1
    else:
      return math.floor((high - low)/2) + 1

  def _get_subarrays(self, arr: List[int]):
    indices = list(range(len(arr)+1))
    for i,j in itertools.combinations(indices,2):
        yield arr[i:j]

  def numOfSubarrays(self, arr: List[int]) -> int:
    result = []
    flag = True
    for x in arr:
      if x % 2 == 1:
        flag = False
        break
    if flag:
      return 0
    for sub in self._get_subarrays(arr):
      s = sum(sub)
      if s % 2 == 1:
        result.append(s)
    # return len(result)%10**9 + 7
    return len(result)

  def numSplits(self, s: str) -> int:
    result = 0
    s_list = list(s)
    for i in range(len(s)-1):
      if len(set(s_list[:(i+1)])) == len(set(s_list[(i+1):])):
        result += 1
    return result

